const mathOperations = { // computation functions
  sum: function (a, b) { // sum fn
    return a + b;        // returns value
  },

  diff: function (a, b) { // subtraction function
    return a - b;
  },
  product: function (a, b) { //product function
    return a * b;
  },
};
module.exports = mathOperations; // exports module
